    private void sendReqBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendReqBtnActionPerformed
        // TODO add your handling code here:
        String message = messageTxt.getText().trim();
        HealthReportRequest hrr = new HealthReportRequest();
        if(!message.isEmpty()){
            
            hrr.setSender(account);
            hrr.setStatus("Waiting");
            hrr.setMessage(message);
        }
        else{
            JOptionPane.showMessageDialog(this, "Enter message", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        Enterprise ent1 = null;
        
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            if(enterprise instanceof HospitalEnterprise){
                ent1 = enterprise;
                break;
            }
        }
        
        for(Organization organization : ent1.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof DoctorOrganization){
                organization.getWorkQueue().getWorkRequestList().add(hrr);
                account.getWorkQueueDoc().getWorkRequestList().add(hrr);
            }
        }
        
        populateTable();
        
    }//GEN-LAST:event_sendReqBtnActionPerformed


