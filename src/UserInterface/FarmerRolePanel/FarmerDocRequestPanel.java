    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel)doctorReqTable.getModel();
        model.setRowCount(0);
        Object[] row = new Object[4];
        for(WorkRequest req : account.getWorkQueueDoc().getWorkRequestList()){
                    row[0] = req.getMessage();
                    row[1] = req;
                    row[2] = req.getResponse();
                    row[3] = req.getRequestDate();
                    model.addRow(row);
        }
        
        
        
    }
